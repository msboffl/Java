package operators;

public class Arithematic {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int addition =  a + b;
        int substraction = a - b;
        int multiplication = a * b;
        int division = a / b; // Quotient

        int modulus = a % b; // remainder

        System.out.println("Addition of a + b is : " + addition);
        System.out.println("Subtraction of a - b is : " + substraction);
        System.out.println("Multiplication of a * b is : " + multiplication);
        System.out.println("Division of a / b is : " + division);
        System.out.println("Modulus of a % b is : " + modulus);

    }


}
